    private void nutsuasinhvienActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        ////////////////////////
        selectedIndex=table.getSelectedRow();
        SinhVien n = sinhvien.get(selectedIndex);
        new Databasecontroller().XoaSV(n.getId());
        if(sinhvien.isEmpty()){
            JOptionPane.showMessageDialog(this,"Không có dữ liệu để sửa");
        }
        else if(selectedIndex==-1){
            JOptionPane.showMessageDialog(this,"Hãy chọn 1 dòng rồi sửa");
        }else{
            SinhVien sv= new SinhVien();
            sv.setMasinhvien(nhapmasinhvien.getText());
            sv.setHovaten(nhaphovaten.getText());
            sv.setNgaysinh(nhapngaysinh.getText());
            String gt = "";
            if (btnam.isSelected()) {
                gt = "Nam";
            } else if (btnu.isSelected()) {
                gt = "Nữ";
            } else {
                gt = "NULL";
            }
            sv.setGioitinh(gt);
            sv.setSodienthoai(nhapsodienthoai.getText());
            sv.setDiachi(nhapdiachi.getText());
            new Databasecontroller().ThemSV(sv);
            showTable();
            JOptionPane.showMessageDialog(this,"Sửa Thành Công");
        }
        //////////////////////
    }
    private void tableMouseClicked(java.awt.event.MouseEvent evt) {
        // TODO add your handling code here:
        /////////////////////////
        selectedIndex=table.getSelectedRow();
        SinhVien sv = sinhvien.get(selectedIndex);
        nhapmasinhvien.setText(sv.getMasinhvien());
        nhaphovaten.setText(sv.getHovaten());
        nhapngaysinh.setText(sv.getNgaysinh());
        String gt1 = sv.getGioitinh();
        if (gt1.equals("Nam")) {
            btnam.setSelected(true);
        } else if (gt1.equals("Nữ")) {
            btnu.setSelected(true);
        } else {
            gt1.equals("NULL");
        }
        nhapsodienthoai.setText(sv.getSodienthoai());
        nhapdiachi.setText(sv.getDiachi());
    }
