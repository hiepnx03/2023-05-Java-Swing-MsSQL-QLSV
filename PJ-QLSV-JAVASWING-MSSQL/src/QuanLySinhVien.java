    private void nutthemsinhvienActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        /////
        SinhVien sv= new SinhVien();
        sv.setMasinhvien(nhapmasinhvien.getText());
        sv.setHovaten(nhaphovaten.getText());
        sv.setNgaysinh(nhapngaysinh.getText());
        String gt = "";
        if(btnam.isSelected()){
            sv.setGioitinh("Nam");
        }else if(btnu.isSelected()){
            sv.setGioitinh("Nữ");
        }else{
            sv.setGioitinh("NULL");
        }
        sv.getGioitinh(gt);
        sv.setSodienthoai(nhapsodienthoai.getText());
        sv.setDiachi(nhapdiachi.getText());
        new Databasecontroller().ThemSV(sv);
        showTable();
        JOptionPane.showMessageDialog(this,"Thêm Thành Công");
        /////
    }

